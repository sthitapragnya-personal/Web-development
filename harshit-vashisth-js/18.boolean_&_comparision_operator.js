//boolean and comparision operator
// <,>,==,===,!=,!==
//----------------------------------------

let n1=5;
let n2=7;
console.log(n1>n2);
console.log(n1<n2);

n1=9;
n2=9;
console.log(n1>n2);
console.log(n1<n2);

console.log(n1==n2);// it only checks the value
console.log(n1===n2);// along with the value it also checks the type

n1="10";
n2=10;
console.log(n1==n2);// it only checks the value
console.log(n1===n2);// along with the value it also checks the type


n1="9";
n2="10";
console.log(n1!=n2);// it only checks the value
console.log(n1!==n2);// along with the value it also checks the type
